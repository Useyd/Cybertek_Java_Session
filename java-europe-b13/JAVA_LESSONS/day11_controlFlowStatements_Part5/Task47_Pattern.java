package day11_controlFlowStatements_Part5;

public class Task47_Pattern {

	public static void main(String[] args) {
		int rows=6;
		int columns=6;
		
		for (int i = 1; i<=rows; i++ ) {
			
			for(int j = 1 ; j<=columns ; j++) {
				
			System.out.print("*"); //write with print first. it will write *'s side by side
			
		}
		
			System.out.println(); // write println for new line as seperator 6 by 6.
			
		}
	}

}

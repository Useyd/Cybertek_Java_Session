package day11_controlFlowStatements_Part5;

		public class DoWhileLoop {

			public static void main(String[] args) {
				
				//do while loop writes one time, even if the condition is false. 

				int i=20; // here 20 is bigger than 10. in while loop it won't be write anythng in output. Bu in Do While loop it will write 20 just once
				
				do {
					
					
					System.out.println(i);
					i++;
					
				}while(i<=10);
			}

		}
	



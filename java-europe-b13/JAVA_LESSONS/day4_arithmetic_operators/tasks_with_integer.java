package day4_arithmetic_operators;

public class tasks_with_integer {

	public static void main(String[] args) {
		
		int i;
		int n;
		
		n = 100;
		i = n;
		
		System.out.println(i);
		System.out.println();
		
		int a,b,c;
		
		a = 10;
		b = 20;
		c = a; //when we assign a to c, a changes as c (10)
		a = b; // when we assign b to a, a changes as b (20)
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println();
		
		
		
		
	
	
	
	
	
	
	}
}

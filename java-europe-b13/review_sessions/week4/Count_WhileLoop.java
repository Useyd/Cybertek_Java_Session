package week4;

public class Count_WhileLoop {

	public static void main(String[] args) {

 
		int counter = 1; 
		
		while (counter<=10) {
			
			System.out.print(counter +" ");
		              counter++;
		
		}System.out.println();
		
		int counter2 = 10;
		
		while (counter2>=1) {
			
			System.out.print(counter2+ " ");
	        counter2--; 
		
		}
		
	}

}

package week2;

public class ordinary_Math {

	public static void main(String[] args) {

		int rate, delta;
		rate = 4;
		delta = 2;
		
	
		System.out.println((rate*rate) + delta);
		System.out.println();
		
		int salary, bonus;
		
		salary=144;
		bonus=54;
		
				
		System.out.println(2*(salary+bonus));
		System.out.println();
		
		double time, mass;
		time = 233333;
		mass = 7;
		
		
		System.out.println(1/(time+(3*mass)));
		System.out.println();
	
		

		double a,t,v;
		a=34;
		t=466;
		v=335;
		
		System.out.println((a-7)/(t+9*v));
	
	
	
	}
	

}

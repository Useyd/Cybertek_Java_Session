package week2;

public class relational_operators {

	public static void main(String[] args) {

		int a = 4;
		int b = 5;
		boolean b1 = (a+4) >= (b-5);
		
		System.out.println(b1);
		
		int x = 5; //one equal sign is assignment
		boolean b2 = x == 5; //two equal sign is boolean
	    System.out.println(b2);	
	}

}

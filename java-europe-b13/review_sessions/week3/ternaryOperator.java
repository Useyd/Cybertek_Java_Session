package week3;

public class ternaryOperator {

	public static void main(String[] args) {

     int num1 = 155; //change and see the new output
     int num2 = 100; //change and see the new output

     int max = (num1>num2) ? num1 : num2;
     //if num1 is bigger is true it will choose n1
     //if num1 is bigger is false it will choose n2
     System.out.println(max);
		
	}

}
